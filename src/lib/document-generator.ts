function pickGuide(productType: ProductType) {
  return guides[productType] ?? guides["其他"];
}

export function generateMockDocument(draft: WorkspaceDraft) {
  const guide = pickGuide(draft.productType);
  const title = `${draft.moduleName || "功能模块"}${draft.documentType}`;
  const keywords = draft.keywords
    ? `本模块重点关注 ${draft.keywords} 等能力，正文可根据页面截图和补充说明继续细化。`
    : "本模块可结合页面截图、字段信息和参考写法进一步补充细节。";

  return `# ${title}

## 功能概述
${guide.overview}${keywords}

## 功能入口
${guide.entry} 若系统配置了角色权限，入口展示和可执行操作会随用户角色发生变化。

## 核心能力
${guide.capabilities.map((item) => `- ${item}：支持用户围绕「${draft.moduleName || "当前功能"}」完成对应信息处理，并在页面中保留清晰的操作反馈。`).join("\n")}

## 操作流程
${guide.flow.map((item, index) => `${index + 1}. ${item}。`).join("\n")}

## 页面说明
${guide.page} 页面应保持信息分区明确，主要操作靠近用户当前处理对象，减少跨页面反复跳转。

## 字段说明
${guide.fields.map((field) => `- ${field}：用于描述或记录当前模块中的关键业务信息，具体是否必填可根据产品配置确定。`).join("\n")}

## 应用场景
${guide.scenario} 对于${draft.targetUser}，文档应重点说明入口位置、关键操作、状态反馈和可能影响后续流程的配置项。

## 注意事项
${guide.notes.map((item) => `- ${item}`).join("\n")}
- 当前内容为通用软件产品文档初稿，不包含真实客户名称、具体行业案例或外部系统数据。`;
}
